function setupHappyNavigationEffects() {
  if (window.__happyNavigationEffectsAttached) {
    return;
  }

  window.__happyNavigationEffectsAttached = true;

  document.addEventListener("click", (event) => {
    if (
      event.defaultPrevented ||
      event.button !== 0 ||
      event.metaKey ||
      event.ctrlKey ||
      event.shiftKey ||
      event.altKey
    ) {
      return;
    }

    const link = event.target.closest("a[href]");
    if (!link) {
      return;
    }

    const rawHref = String(link.getAttribute("href") || "").trim();
    if (
      !rawHref ||
      rawHref.charAt(0) === "#" ||
      rawHref.toLowerCase().startsWith("javascript:")
    ) {
      return;
    }

    if (link.target && link.target !== "_self") {
      return;
    }

    event.preventDefault();
    if (happyTransitionLock) {
      return;
    }

    happyTransitionLock = true;
    replayClassAnimation(link, "happy-click-burst", 320);

    const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
    window.setTimeout(() => {
      document.body.classList.add("happy-nav-leaving");
    }, reducedMotion ? 0 : 85);

    window.setTimeout(() => {
      window.location.href = link.href;
    }, reducedMotion ? 90 : 430);
  });
}

function pickDynamicVerses(causeKey, count) {
  const pool = normalizeVerses(versesData[causeKey]);
  if (!pool.length) {
    return [];
  }

  const desired = Math.max(1, Math.min(count || 9, pool.length));
  return shuffleList(pool).slice(0, desired);
}

function renderDynamicVerses(causeKey, options) {
  const container = document.querySelector(".verse-container");
  if (!container || !causeKey) {
    return;
  }

  const opts = options || {};
  const count = typeof opts.count === "number" ? opts.count : 9;
  const available = normalizeVerses(versesData[causeKey]);

  if (!available.length) {
    container.innerHTML = "";
    return;
  }

  const render = () => {
    const selected = pickDynamicVerses(causeKey, count);
    container.innerHTML = "";

    selected.forEach((verse) => {
      const link = document.createElement("a");
      link.className = "verse-button";
      link.textContent = verse.text;

      if (verse.href && verse.href !== "#") {
        link.href = verse.href;
      } else {
        link.href = "javascript:void(0)";
        link.style.cursor = "default";
        link.style.opacity = "0.92";
      }

      container.appendChild(link);
    });

    replayClassAnimation(container, "happy-verse-refresh", 360);
  };

  render();

  const existingRefresh = document.querySelector(".verse-refresh-button");
  if (existingRefresh) {
    existingRefresh.remove();
  }

  if (available.length > 1) {
    const refreshButton = document.createElement("button");
    refreshButton.type = "button";
    refreshButton.className = "verse-button verse-refresh-button";
    refreshButton.textContent = typeof opts.refreshLabel === "string" && opts.refreshLabel.trim()
      ? opts.refreshLabel.trim()
      : "Get new verse";
    refreshButton.style.display = "inline-block";
    refreshButton.style.marginBottom = "18px";
    refreshButton.style.width = "auto";
    refreshButton.style.minWidth = "130px";
    refreshButton.style.padding = "10px 16px";
    refreshButton.style.fontSize = "0.95em";
    refreshButton.style.borderRadius = "18px";
    refreshButton.style.fontWeight = "700";

    refreshButton.addEventListener("click", () => {
      replayClassAnimation(refreshButton, "happy-click-burst", 320);
      render();
    });
    container.parentNode.insertBefore(refreshButton, container);
  }
}

window.versesData = versesData;
window.renderDynamicVerses = renderDynamicVerses;

document.addEventListener("DOMContentLoaded", () => {
  injectHappyEffectsStyles();
  setupHappyNavigationEffects();

  const pageCause = (document.body && document.body.dataset && document.body.dataset.cause)
    ? document.body.dataset.cause
    : decodeURIComponent(window.location.pathname.split("/").pop().split(".")[0]);

  if (!pageCause || !versesData[pageCause]) {
    return;
  }

  renderDynamicVerses(pageCause, { count: 9, refreshLabel: "Get new verse" });
});
