let happyTransitionLock = false;

function shuffleList(list) {
  const copy = list.slice();
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

function normalizeVerses(rawList) {
  return (rawList || [])
    .map((item) => ({
      text: String(item && item.text ? item.text : "").trim(),
      href: String(item && item.href ? item.href : "").trim()
    }))
    .filter((item) => item.text.length > 0);
}

function replayClassAnimation(element, className, durationMs) {
  if (!element) {
    return;
  }

  element.classList.remove(className);
  void element.offsetWidth;
  element.classList.add(className);

  window.setTimeout(() => {
    element.classList.remove(className);
  }, durationMs || 360);
}

