function formatVerseReference(rawReference) {
  const raw = String(rawReference || "").trim();
  if (!raw) {
    return "";
  }

  const cleaned = raw
    .replace(/;/g, ":")
    .replace(/\.(\d+)/g, ":$1")
    .replace(/^deutoronomy\b/i, "deuteronomy")
    .toLowerCase();

  return cleaned.replace(/\b[a-z]/g, (char) => char.toUpperCase());
}

function toMessageSlug(reference) {
  return String(reference || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function toMessageHref(reference) {
  const slug = toMessageSlug(reference);
  return slug ? `../message/${slug}.html` : "";
}

